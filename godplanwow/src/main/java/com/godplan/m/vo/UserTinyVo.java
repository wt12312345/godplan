package com.godplan.m.vo;

public class UserTinyVo {
	private long id = 0;
	private String name = "";
	private int num = 0;

	public long getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public int getNum() {
		return num;
	}

	public void setId(long id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
